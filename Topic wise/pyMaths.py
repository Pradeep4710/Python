# Various maths operations and functions provided by the math library

import math

pi = 3.14

print(round(pi)) # prints the rounded off number to the nearest integer
print(math.ceil(pi))  # prints the rounded off number to the upper integer
print(math.floor(pi)) # prints the rounded off number to the lower integer

print(abs(pi)) # shows how far a number is to 0

print(pow(6,2)) # prints the result of number to the given power

print(math.sqrt(81)) # prints the square root of given number

x = 2
y = 4
z = 6

print(max(x,y,z)) # prints the maximum number out of the given inputs
print(min(x,y,z)) # prints the minimum number out of the given inputs